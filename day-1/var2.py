# Variable with assign multiple valus
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

# You can also use the + operator to output multiple variables:

x = "Python "
y = "is "
z = "awesome"
print(x + y + z)

# For numbers, the + character works as a mathematical operator:

x = 5
y = 10
print(x + y)